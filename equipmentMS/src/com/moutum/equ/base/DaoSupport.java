public interface DaoSupport<T>
{
    /********************************************************************************
     * 保存实体
     * 
     * @param entity
     ********************************************************************************/
    void save(T entity);

    /********************************************************************************
     * 删除实体
     * 
     * @param id
     ********************************************************************************/
    void delete(Long id);

    /********************************************************************************
     * 更新实体
     * 
     * @param entity
     ********************************************************************************/
    void update(T entity);

    /********************************************************************************
     * 按id查询
     * 
     * @param id
     * @return
     ********************************************************************************/
    T getById(Long id);

    /********************************************************************************
     * 按id查询
     * 
     * @param ids
     * @return
     ********************************************************************************/
    List<T> getByIds(Long[] ids);

    /********************************************************************************
     * 查询所有
     * 
     * @return
     ********************************************************************************/
    List<T> findAll();

    /********************************************************************************
     * 公共的查询分页信息的方法
     * 
     * @param pageNum:当前页号码
     * @param pageSize:每页数据量
     * @param queryHelper:HQL语句与参数列表
     * @return
     ********************************************************************************/
    PageBean getPageBean(int pageNum, int pageSize, QueryHelper queryHelper);
}